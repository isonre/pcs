package com.yunforge.common.bean;

import java.util.List;

/**
 * @ClassName: JqGridSearchBean
 * @Description: TODO
 * @author Oliver Wen
 * @date 2015年10月7日 上午11:18:39
 * 
 */
public class JqGridSearchBean implements java.io.Serializable {
	private static final long serialVersionUID = -5867110687657615120L;
	private String groupOp; // 多字段查询时分组类型，主要是AND或者OR
	private List<JqGridSearchDetailBean> rules; // 多字段查询时候，查询条件的集合

	private String searchField; // 单字段查询的时候，查询字段名称
	private String searchString; // 单字段查询的时候，查询字段的值
	private String searchOper;

	public JqGridSearchBean() {

	}

	public String getGroupOp() {
		return groupOp;
	}

	public void setGroupOp(String groupOp) {
		this.groupOp = groupOp;
	}

	public List<JqGridSearchDetailBean> getRules() {
		return rules;
	}

	public void setRules(List<JqGridSearchDetailBean> rules) {
		this.rules = rules;
	}

	public String getSearchField() {
		return searchField;
	}

	public void setSearchField(String searchField) {
		this.searchField = searchField;
	}

	public String getSearchString() {
		return searchString;
	}

	public void setSearchString(String searchString) {
		this.searchString = searchString;
	}

	public String getSearchOper() {
		return searchOper;
	}

	public void setSearchOper(String searchOper) {
		this.searchOper = searchOper;
	}

}
