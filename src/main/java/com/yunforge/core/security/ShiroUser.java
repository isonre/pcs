package com.yunforge.core.security;

import java.io.Serializable;

/**
 * @ClassName: ShiroUser
 * @Description: TODO
 * @author Oliver Wen
 * @date 2015年10月5日 上午9:39:13
 * 
 */
public class ShiroUser implements Serializable {
	private static final long serialVersionUID = 6745361599397689360L;
	private String uid;
	private String username;
	private String fullName;
	private String divCode;
	private String orgId;
	private String orgCode;
	private String avatar;
	private boolean admin;

	/**
	 * @param uid
	 * @param username
	 * @param fullName
	 * @param divCode
	 * @param orgId
	 */
	public ShiroUser(String uid, String username, String fullName,
			String divCode, String orgId, String orgCode,String avatar, boolean admin) {
		super();
		this.uid = uid;
		this.username = username;
		this.fullName = fullName;
		this.divCode = divCode;
		this.orgId = orgId;
		this.orgCode = orgCode;
		this.avatar = avatar;
		this.admin = admin;
	}

	public String getUid() {
		return uid;
	}

	public void setUid(String uid) {
		this.uid = uid;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getFullName() {
		return fullName;
	}

	public void setFullName(String fullName) {
		this.fullName = fullName;
	}

	public String getDivCode() {
		return divCode;
	}

	public void setDivCode(String divCode) {
		this.divCode = divCode;
	}

	public String getOrgId() {
		return orgId;
	}

	public void setOrgId(String orgId) {
		this.orgId = orgId;
	}

	public String getOrgCode() {
		return orgCode;
	}

	public void setOrgCode(String orgCode) {
		this.orgCode = orgCode;
	}

	public String getAvatar() {
		return avatar;
	}

	public void setAvatar(String avatar) {
		this.avatar = avatar;
	}

	public boolean isAdmin() {
		return admin;
	}

	public void setAdmin(boolean admin) {
		this.admin = admin;
	}

}
